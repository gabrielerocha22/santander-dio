/*Criar aplicação que calcule a area de 3 quadriláteros notáveis: Quadrado, retângulo e trapézio*/

package exercicio.metodo;

public class AreaQuadrilatero {

	public static void calculaAreaQuadrado(int a, int b) {
		int areaQuadrado = a*b;
		System.out.println("A área do quadrado é: " + areaQuadrado);
		
		
 }
	public static void calculaAreaRetangulo(int a, int b) {
		int areaRetangulo = a*b;
		System.out.println("A área do quadrado é: " + areaRetangulo);
		
		
 }
	public static void calculaAreatrapezio(int b1, int b2, int a) {
		int areatrapezio = ((b1+b2)*a)/2;
		System.out.println("A área do quadrado é: " + areatrapezio);
		
		
 }
	
	
	
	

}
